    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function stepTwo(Request $request)
    {
        $request->validate([
            'course_code' => 'required|string',
            'duration' => 'required|integer',
            'course_fee' => 'required|numeric',
        ]);

        // Store course data in the session
        $request->session()->put('course', $request->only(['course_code', 'duration', 'course_fee']));

        // Log session data for debugging
        \Log::info('Setting session data:', [
            'course' => $request->only(['course_code', 'duration', 'course_fee']),
            'student' => $request->session()->get('student'), // Log the student data
        ]);

        return redirect()->route('payments.payment')->with('success', 'Course selected successfully!');
    }

